export type Language = keyof typeof invoiceTranslations;

export function getInvoiceTranslation(lang: string, key: string): string {
  const supportedLang = (lang in invoiceTranslations ? lang : 'en') as Language;
  const translations = invoiceTranslations[supportedLang];
  return (translations as any)[key] || key;
}